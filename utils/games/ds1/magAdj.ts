// Helper function for lookup
export function getMagicAdjustment(
  item: string,
  stat: number,
  isCatalyst: boolean = false
): number | null {
  const meta = isCatalyst
    ? CATALYST_MAGADJ_META[item]
    : TALISMAN_MAGADJ_META[item];
  if (!meta) return null;
  const table = isCatalyst
    ? CATALYST_MAGADJ_TABLES[item]
    : TALISMAN_MAGADJ_TABLES[item];
  return table?.[stat] ?? null;
}
